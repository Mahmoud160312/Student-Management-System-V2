    private void btn_viewLevelExamAttendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewLevelExamAttendanceActionPerformed

        if (checkEmpty(cb_level3) && checkEmpty(cb_examCode2) && checkEmpty(cb_group2)) {
            String level = cb_level3.getSelectedItem().toString();
            String group = cb_group2.getSelectedItem().toString();
            int exam_code = Integer.parseInt(cb_examCode2.getSelectedItem().toString());
            PreparedStatement stmt;

            try {
                stmt = con.prepareStatement("select * from student where student_status!=? and level_name=? and student_code not in(select student_code from student_exam_connection where exam_code=? and level_name=?)and student_code in(select student_code from student_group_connection where group_name=? and level_name=? )");
                stmt.setString(1, "موقوف");
                stmt.setString(2, level);
                stmt.setInt(3, exam_code);
                stmt.setString(4, level);
                stmt.setString(5, group);
                stmt.setString(6, level);

                ResultSet set = stmt.executeQuery();

                DefaultTableModel dm = new DefaultTableModel();

                if (chb_studentPhone2.isSelected()) {
                    dm.addColumn("رقم هاتف الطالب");
                }
                if (chb_level2.isSelected()) {
                    dm.addColumn("الفرقة");
                }
                if (chb_parentJob2.isSelected()) {
                    dm.addColumn("مهنة ولي الامر");
                }
                if (chb_parentPhone2.isSelected()) {
                    dm.addColumn("رقم ولي الامر");
                }
                if (chb_parentName2.isSelected()) {
                    dm.addColumn("اسم ولي الامر");
                }
                if (chb_studentName2.isSelected()) {
                    dm.addColumn("الاسم");
                }
                dm.addColumn("الكود");
                while (set.next()) {
                    ArrayList<String> r2 = new ArrayList<>();

                    if (chb_studentPhone2.isSelected()) {
                        r2.add(set.getString(3));
                    }
                    if (chb_level2.isSelected()) {
                        r2.add(set.getString(9));
                    }
                    if (chb_parentJob2.isSelected()) {
                        r2.add(set.getString(7));
                    }
                    if (chb_parentPhone2.isSelected()) {
                        r2.add(set.getString(6));
                    }
                    if (chb_parentName2.isSelected()) {
                        r2.add(set.getString(5));
                    }
                    if (chb_studentName2.isSelected()) {
                        r2.add(set.getString(2));
                    }
                    r2.add(set.getString(1));
                    String r[] = r2.toArray(new String[r2.size()]);
                    dm.addRow(r);
                }
                t_viewStudentExamAttendance.setModel(dm);
                printTableDetermination = 0;

            } catch (SQLException ex) {
                handleSqlExceptions(ex.getErrorCode());
            }
        } else {
            DefaultTableModel dtm = new DefaultTableModel();
            t_viewStudentExamAttendance.setModel(dtm);
        }
    }//GEN-LAST:event_btn_viewLevelExamAttendanceActionPerformed

