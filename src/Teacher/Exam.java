    private void btn_viewLevelExamDegreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewLevelExamDegreeActionPerformed
        if (checkEmpty(cb_level2) && checkEmpty(cb_examCode1) && checkEmpty(cb_group1)) {
            String level = cb_level2.getSelectedItem().toString();
            String group = cb_group1.getSelectedItem().toString();
            int exam_code = Integer.parseInt(cb_examCode1.getSelectedItem().toString());
            PreparedStatement stmt;

            try {
                stmt = con.prepareStatement("select * from student_exam_connection where student_code in (select student_code from student where student_status!=?) and student_code in(select student_code from student_group_connection where group_name=? and level_name=?) and exam_code=? and level_name=? order by student_degree desc");
                stmt.setString(1, "موقوف");
                stmt.setString(2, group);
                stmt.setString(3, level);
                stmt.setInt(4, exam_code);
                stmt.setString(5, level);
                ResultSet set = stmt.executeQuery();

                DefaultTableModel dm = new DefaultTableModel();
                if (chb_time.isSelected()) {
                    dm.addColumn("الوقت");
                }
                if (chb_date.isSelected()) {
                    dm.addColumn("التاريخ");
                }
                if (chb_level.isSelected()) {
                    dm.addColumn("الفرقة");
                }
                if (chb_examDegree.isSelected()) {
                    dm.addColumn("درجة الامتحان");
                }
                if (chb_examName.isSelected()) {
                    dm.addColumn("اسم الامتحان");
                }
                if (chb_examCode.isSelected()) {
                    dm.addColumn("كود الامتحان");
                }

                dm.addColumn("درجة الطالب");
                dm.addColumn("اسم الطالب");
                dm.addColumn("كود الطالب");

                while (set.next()) {
                    ArrayList<String> r2 = new ArrayList<>();
                    if (chb_time.isSelected()) {
                        r2.add(set.getString(9));
                    }
                    if (chb_date.isSelected()) {
                        r2.add(set.getString(8));
                    }
                    if (chb_level.isSelected()) {
                        r2.add(set.getString(2));
                    }
                    if (chb_examDegree.isSelected()) {
                        r2.add(set.getString(6));
                    }
                    if (chb_examName.isSelected()) {
                        r2.add(set.getString(4));
                    }
                    if (chb_examCode.isSelected()) {
                        r2.add(set.getString(1));
                    }

                    r2.add(set.getString(7));
                    r2.add(set.getString(5));
                    r2.add(set.getString(3));

                    String r[] = r2.toArray(new String[r2.size()]);
                    dm.addRow(r);
                }
                t_viewStudentExamDegree.setModel(dm);
                printTableDetermination = 0;
            } catch (SQLException ex) {
                handleSqlExceptions(ex.getErrorCode());
            }
        } else {
            DefaultTableModel dtm = new DefaultTableModel();
            t_viewStudentExamDegree.setModel(dtm);
        }
    }//GEN-LAST:event_btn_viewLevelExamDegreeActionPerformed

