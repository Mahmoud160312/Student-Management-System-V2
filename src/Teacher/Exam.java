    private void tf_studentDegreeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_studentDegreeKeyPressed
        if (enter(evt.getKeyCode())) {
            if (checkEmpty(cb_level1) && checkEmpty(cb_examCode) && checkEmpty(tf_studentCode) && checkEmpty(tf_studentName) && checkEmpty(tf_studentDegree) && checkExceedStudentDegree(tf_studentDegree, tf_examDegree1)) {
                int student_code = Integer.parseInt(tf_studentCode.getText());
                String level = cb_level1.getSelectedItem().toString();
                double exam_degree = Double.parseDouble(tf_examDegree1.getText());
                int exam_code = Integer.parseInt(cb_examCode.getSelectedItem().toString());
                double student_degree = Double.parseDouble(tf_studentDegree.getText());
                String student_name = tf_studentName.getText();
                String exam_name = tf_examName1.getText();
                String date = getDate();
                String time = getTime();
                PreparedStatement stmt;
                try {
                    stmt = con.prepareStatement("insert into student_exam_connection values(?,?,?,?,?,?,?,?,?)");
                    stmt.setInt(1, exam_code);
                    stmt.setString(2, level);
                    stmt.setInt(3, student_code);
                    stmt.setString(4, exam_name);
                    stmt.setString(5, student_name);
                    stmt.setDouble(6, exam_degree);
                    stmt.setDouble(7, student_degree);
                    stmt.setString(8, date);
                    stmt.setString(9, time);
                    stmt.execute();
                    addMessage();
                    recordEvent("اضافة", "اضافة درجات امتحان", "تم اضافة درجة امتحان للطالب " + student_name + "في الفرقة" + level);
                    loadStudentDegreeInExam(t_allExamDegree, level, exam_code);
                    cleanAfterAddStudentDegree();

                } catch (SQLException ex) {
                    handleSqlExceptions(ex.getErrorCode());
                }
            }
        }
    }//GEN-LAST:event_tf_studentDegreeKeyPressed

