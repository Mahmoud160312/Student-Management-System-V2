    private void btn_viewGroupAttendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewGroupAttendanceActionPerformed
        if (checkEmpty(cb_level) && checkEmpty(tf_studentCode) && checkEmpty(tf_studentName) && checkEmpty(dc_startDate) && checkEmpty(dc_endDate)) {
            String level_name = cb_level.getSelectedItem().toString();
            int student_code = Integer.parseInt(tf_studentCode.getText());
            String student_name = tf_studentName.getText();
            String student_status = "";
            String first_date = ((JTextField) dc_startDate.getDateEditor().getUiComponent()).getText();
            String second_date = ((JTextField) dc_endDate.getDateEditor().getUiComponent()).getText();
            String class_start_date = "";
            int class_number_in_class_table = 0;
            int class_number_in_attendance_table = 0;
            DefaultTableModel dtm = new DefaultTableModel();

            // get the total number of classes and number of classes the student attend and apsence
            int totalCount = 0, attendanceCount = 0, apsenceCount = 0;
            PreparedStatement st_totalCount, st_attendanceCount, st_getClasses, st_getStudentAttendance;
            ResultSet res_totalCount, res_attendanceCount, res_getClasses, res_getStudentAttendance;
            try {
                // get the total number of classes in a level
                st_totalCount = con.prepareStatement("select count(class_number) from class where class.start_date between cast(? as date)  and cast(? as date) and level_name=?");
                st_totalCount.setString(1, first_date);
                st_totalCount.setString(2, second_date);
                st_totalCount.setString(3, level_name);
                res_totalCount = st_totalCount.executeQuery();
                while (res_totalCount.next()) {
                    totalCount = res_totalCount.getInt(1);
                }

                // get the total number of classes the student attend
                st_attendanceCount = con.prepareStatement("select count(student_code) from student_class_connection where student_code =? and attendance_date between cast(? as date)  and cast(? as date) and level_name=?;");
                st_attendanceCount.setInt(1, student_code);
                st_attendanceCount.setString(2, first_date);
                st_attendanceCount.setString(3, second_date);
                st_attendanceCount.setString(4, level_name);
                res_attendanceCount = st_attendanceCount.executeQuery();
                while (res_attendanceCount.next()) {
                    attendanceCount = res_attendanceCount.getInt(1);
                }
                apsenceCount = totalCount - attendanceCount;

                // get all the class added to this level
                st_getClasses = con.prepareStatement("select * from class where level_name=? and start_date between cast(? as date) and cast(? as date)");
                st_getClasses.setString(1, level_name);
                st_getClasses.setString(2, first_date);
                st_getClasses.setString(3, second_date);
                res_getClasses = st_getClasses.executeQuery();
                // add column to the table
                dtm.addColumn("التاريخ");
                dtm.addColumn("حالة الطالب");
                dtm.addColumn("رقم الحصة");
                // check if this classes the student attend or not
                while (res_getClasses.next()) {
                    class_number_in_class_table = res_getClasses.getInt("class_number");
                    class_start_date = res_getClasses.getString("start_date");
                    st_getStudentAttendance = con.prepareStatement("select * from student_class_connection where level_name=? and student_code=? and class_number=?");
                    st_getStudentAttendance.setString(1, level_name);
                    st_getStudentAttendance.setInt(2, student_code);
                    st_getStudentAttendance.setInt(3, class_number_in_class_table);
                    res_getStudentAttendance = st_getStudentAttendance.executeQuery();

                    while (res_getStudentAttendance.next()) {
                        class_number_in_attendance_table = res_getStudentAttendance.getInt("class_number");
                        student_status = res_getStudentAttendance.getString("student_status");
                        String r[] = {class_start_date,
                            student_status,
                            String.valueOf(class_number_in_class_table),};
                        dtm.addRow(r);
                    }

                    // check if the student doesn't attend to a specific class and add it to the table
                    if (class_number_in_class_table != class_number_in_attendance_table) {
                        student_status = "غائب";
                        String r[] = {class_start_date,
                            student_status,
                            String.valueOf(class_number_in_class_table),
                            student_name,
                            String.valueOf(student_code)
                        };
                        dtm.addRow(r);
                    }
                }

                tf_classTotalNumber.setText(String.valueOf(totalCount));
                tf_classAttendanceNumber.setText(String.valueOf(attendanceCount));
                tf_classApsenceNumber.setText(String.valueOf(apsenceCount));
                t_studentAttendanceReport.setModel(dtm);
                printAttendanceTable = 1;

            } catch (SQLException ex) {
                handleSqlExceptions(ex.getErrorCode());
            }
        }
    }//GEN-LAST:event_btn_viewGroupAttendanceActionPerformed

