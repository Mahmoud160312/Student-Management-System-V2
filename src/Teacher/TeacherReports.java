    private void btn_viewQuizReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewQuizReportActionPerformed
        if (checkEmpty(cb_level1) && checkEmpty(tf_studentCode1) && checkEmpty(tf_studentName1) && checkEmpty(dc_startDate1) && checkEmpty(dc_endDate1)) {
            String level_name = cb_level1.getSelectedItem().toString();
            int student_code = Integer.parseInt(tf_studentCode1.getText());
            String student_name = tf_studentName1.getText();
            String student_status_or_degree = "";
            String first_date = ((JTextField) dc_startDate1.getDateEditor().getUiComponent()).getText();
            String second_date = ((JTextField) dc_endDate1.getDateEditor().getUiComponent()).getText();
            String quiz_start_date = "";
            int quiz_number_in_quiz_table = 0;
            int quiz_number_in_quiz_connection_table = 0;
            String quiz_name;
            String quiz_degree;
            DefaultTableModel dtm = new DefaultTableModel();

            // get the total number of classes and number of classes the student attend and apsence
            int totalCount = 0, attendanceCount = 0, apsenceCount = 0;
            PreparedStatement st_totalCount, st_attendanceCount, st_getQuiz, st_getStudentQuizDegree;
            ResultSet res_totalCount, res_attendanceCount, res_getQuiz, res_getStudentQuizDegree;
            try {
                // get the total number of quiz in a level
                st_totalCount = con.prepareStatement("select count(quiz_code) from quiz where quiz_date between cast(? as date)  and cast(? as date) and level_name=?");
                st_totalCount.setString(1, first_date);
                st_totalCount.setString(2, second_date);
                st_totalCount.setString(3, level_name);
                res_totalCount = st_totalCount.executeQuery();
                while (res_totalCount.next()) {
                    totalCount = res_totalCount.getInt(1);
                }

                // get the total number of quiz the student attend
                st_attendanceCount = con.prepareStatement("select count(quiz_code) from student_quiz_connection where student_code =? and date between cast(? as date)  and cast(? as date) and level_name=?;");
                st_attendanceCount.setInt(1, student_code);
                st_attendanceCount.setString(2, first_date);
                st_attendanceCount.setString(3, second_date);
                st_attendanceCount.setString(4, level_name);
                res_attendanceCount = st_attendanceCount.executeQuery();
                while (res_attendanceCount.next()) {
                    attendanceCount = res_attendanceCount.getInt(1);
                }
                apsenceCount = totalCount - attendanceCount;

                // get all the class added to this level
                st_getQuiz = con.prepareStatement("select * from quiz where level_name=? and quiz_date between cast(? as date) and cast(? as date)");
                st_getQuiz.setString(1, level_name);
                st_getQuiz.setString(2, first_date);
                st_getQuiz.setString(3, second_date);
                res_getQuiz = st_getQuiz.executeQuery();
                // add column to the table
                dtm.addColumn("التاريخ");
                dtm.addColumn("درجة التسميع");
                dtm.addColumn("درجة الطالب");
                dtm.addColumn("اسم التسميع");
                dtm.addColumn("كود التسميع");

                // check if this classes the student attend or not
                while (res_getQuiz.next()) {
                    quiz_number_in_quiz_table = res_getQuiz.getInt("quiz_code");
                    quiz_start_date = res_getQuiz.getString("quiz_date");
                    quiz_degree = res_getQuiz.getString("quiz_degree");
                    quiz_name = res_getQuiz.getString("quiz_name");
                    st_getStudentQuizDegree = con.prepareStatement("select * from student_quiz_connection where level_name=? and student_code=? and quiz_code=?");
                    st_getStudentQuizDegree.setString(1, level_name);
                    st_getStudentQuizDegree.setInt(2, student_code);
                    st_getStudentQuizDegree.setInt(3, quiz_number_in_quiz_table);
                    res_getStudentQuizDegree = st_getStudentQuizDegree.executeQuery();

                    while (res_getStudentQuizDegree.next()) {
                        quiz_number_in_quiz_connection_table = res_getStudentQuizDegree.getInt("quiz_code");
                        student_status_or_degree = res_getStudentQuizDegree.getString("student_degree");
                        String r[] = {quiz_start_date,
                            quiz_degree,
                            student_status_or_degree,
                            quiz_name,
                            String.valueOf(quiz_number_in_quiz_table),};
                        dtm.addRow(r);
                    }

                    // check if the student doesn't attend to a specific class and add it to the table
                    if (quiz_number_in_quiz_table != quiz_number_in_quiz_connection_table) {
                        student_status_or_degree = "غائب";
                        String r[] = {quiz_start_date,
                            quiz_degree,
                            student_status_or_degree,
                            quiz_name,
                            String.valueOf(quiz_number_in_quiz_table),
                            student_name,
                            String.valueOf(student_code)
                        };
                        dtm.addRow(r);
                    }
                }

                tf_quiztotalNumber.setText(String.valueOf(totalCount));
                tf_quizAttendanceNumber.setText(String.valueOf(attendanceCount));
                tf_quizApsenceNumber.setText(String.valueOf(apsenceCount));
                t_studentQuizDegreeReport.setModel(dtm);
                printQuizTable = 1;

            } catch (SQLException ex) {
                handleSqlExceptions(ex.getErrorCode());
            }
        }
    }//GEN-LAST:event_btn_viewQuizReportActionPerformed

