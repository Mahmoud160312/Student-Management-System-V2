    private void btn_copyStudentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_copyStudentsActionPerformed
        // check if the two level is selected
        if (checkEmpty(cb_level) && checkEmpty(cb_level2)) {
            String fromThisLevel = cb_level.getSelectedItem().toString();
            String toThisLevel = cb_level2.getSelectedItem().toString();
            String student_level = toThisLevel;
            String date = getDate();
            String status = "غير موقوف";
            String stop_date = "";
            {
                int yes = JOptionPane.showConfirmDialog(null,
                        "سوف تقوم بنسخ كل الطلاب الموجودين في الفرقة المختاره من اليمين الي اليس الرجاء التاكد من انك تريد ذلك حقا "
                        + "\n" + "ملحوظة:"
                        + "\n" + "لا يمكن التراجع عن الاضافة الا يدويا",
                        "تحذير",
                        JOptionPane.YES_NO_OPTION);
                // if accept the confirmation messege
                if (yes == JOptionPane.YES_OPTION) {
                    try {
                        // get the total number of the student in the new level it should be empty
                        PreparedStatement stmt = con.prepareStatement("select count(student_code) as count from student where level_name=?");
                        stmt.setString(1, toThisLevel);
                        ResultSet set = stmt.executeQuery();
                        set.next();
                        // first check if the second level is totaly empty
                        if (set.getInt("count") == 0) {
                            this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
                            PreparedStatement stmt2 = con.prepareStatement("select * from student where student_status!=? and level_name=?");
                            stmt2.setString(1, "موقوف");
                            stmt2.setString(2, fromThisLevel);
                            ResultSet set2 = stmt2.executeQuery();
                            // insert the student in the new level
                            while (set2.next()) {
                                int student_code = (set2.getInt(1));
                                String student_name = set2.getString(2);
                                int student_Phone = (set2.getInt(3));
                                String student_address = set2.getString(4);
                                String parent_name = set2.getString(5);
                                int parent_Phone = (set2.getInt(6));
                                String parent_job = set2.getString(7);
                                String relativity = set2.getString(8);
                                if (checkExceedLevelWhenAddStudent(student_level)) {
                                    // adding student information to student table
                                    try {
                                        PreparedStatement stmt3 = con.prepareStatement("insert into student values(?,?,?,?,?,?,?,?,?,?,?,?)");
                                        stmt3.setInt(1, student_code);
                                        stmt3.setString(2, student_name);
                                        stmt3.setInt(3, student_Phone);
                                        stmt3.setString(4, student_address);
                                        stmt3.setString(5, parent_name);
                                        stmt3.setInt(6, parent_Phone);
                                        stmt3.setString(7, parent_job);
                                        stmt3.setString(8, relativity);
                                        stmt3.setString(9, student_level);
                                        stmt3.setString(10, date);
                                        stmt3.setString(11, status);
                                        stmt3.setString(12, stop_date);
                                        stmt3.execute();
                                    } catch (SQLException ex) {
                                        handleSqlExceptions(ex.getErrorCode());
                                    }
                                }
                            }
                            this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
                            recordEvent("نسخ", "نسخ الطلاب من فرقة لاخرى", "تم نسخ الطلاب من الفرقة " + fromThisLevel + " " + " الى الفرقة" + toThisLevel);
                            jOptionPaneMessage("تم النسخ الطلاب في الفرقة بنجاح", 1);
                        } else {
                            jOptionPaneMessage("تاكد من ان الفرقة المراد النسخ فيها خاليه تماما", 0);
                        }
                    } catch (SQLException ex) {
                        handleSqlExceptions(ex.getErrorCode());
                    }
                }
            }
        }
    }//GEN-LAST:event_btn_copyStudentsActionPerformed

