    private void tf_timeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_timeKeyPressed
        if (enter(evt.getKeyChar())) {
            // add group data
            if (checkEmpty(tf_groupName) && checkEmpty(tf_maxNumber) && checkEmpty(tf_time) && checkEmpty(cb_level1)) {
                String level_name = cb_level1.getSelectedItem().toString();
                String group_name = tf_groupName.getText();
                String time = tf_time.getText();
                int maxNumber = Integer.parseInt(tf_maxNumber.getText());
                if (checkExceedLevelWhenAddOrUpdateGroup(maxNumber, level_name, "add", group_name)) {

                    try {
                        PreparedStatement stmt = con.prepareStatement("INSERT INTO student_group VALUES (?,?,?,?)");
                        stmt.setString(1, group_name);
                        stmt.setInt(2, maxNumber);
                        stmt.setString(3, time);
                        stmt.setString(4, level_name);

                        stmt.execute();
                        addMessage();
                        cleanGroupPanel();
                    } catch (SQLException ex) {
                        handleSqlExceptions(ex.getErrorCode());
                    }
                    // refresh the table of groups
                    loadGroup(t_allGroup, level_name);
                }
            }
        }
    }//GEN-LAST:event_tf_timeKeyPressed

