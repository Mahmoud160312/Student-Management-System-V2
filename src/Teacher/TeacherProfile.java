    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        int yes = JOptionPane.showConfirmDialog(null, "هل انت متاكد من انك تريد تغيير الكود و الاسم ", "استفهام", JOptionPane.YES_NO_OPTION, 1);
        if (yes == JOptionPane.YES_OPTION) {
            if (checkEmpty(tf_code) && checkEmpty(tf_teacherName)) {
                String admin_code = tf_code.getText();
                String admin_name = tf_teacherName.getText();
                PreparedStatement stmt;
                try {
                    stmt = adminCon.prepareStatement("update admin set admin_code=?,admin_name=?, admin_password=? where admin_code=?");
                    stmt.setString(1, admin_code);
                    stmt.setString(2, admin_name);
                    stmt.setString(3, loadHolderPassword());
                    stmt.setString(4, loadHolderCode());
                    stmt.execute();
                    updateMessage();
                    updateHolder(admin_code, admin_name, loadHolderPassword());
                } catch (SQLException ex) {
                    handleSqlExceptions(ex.getErrorCode());
                }
            }
        }
    }//GEN-LAST:event_btn_saveActionPerformed

