    private void btn_updatePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updatePasswordActionPerformed
        // check that the important field isn't empty
        int yes = JOptionPane.showConfirmDialog(null, "هل انت متاكد من انك تريد تغيير كلمة المرور ", "استفهام", JOptionPane.YES_NO_OPTION, 1);
        if (yes == JOptionPane.YES_OPTION) {
            if (checkEmpty(tf_oldTeacherPassword) && checkEmpty(tf_newTeacherPassword) && checkEmpty(tf_confirmTeacherPassword)) {
                String admin_code = loadHolderCode();
                String admin_name = loadHolderName();
                String currentPassword = tf_oldTeacherPassword.getText();
                String newPassword = tf_newTeacherPassword.getText();
                String confrimationPassword = tf_confirmTeacherPassword.getText();
                PreparedStatement stmt;
                if (currentPassword.equals(loadHolderPassword())) {
                    if (newPassword.equals(confrimationPassword)) {
                        try {
                            stmt = adminCon.prepareStatement("update admin set  admin_password=? where admin_code=?");
                            stmt.setString(1, newPassword);
                            stmt.setString(2, admin_code);
                            stmt.execute();
                            updateMessage();
                            updateHolder(admin_code, admin_name, newPassword);
                            cleanAfterUpdatePassword();
                        } catch (SQLException ex) {
                            handleSqlExceptions(ex.getErrorCode());
                        }
                    } else {
                        makeRed(tf_confirmTeacherPassword);
                        jOptionPaneMessage("كلمة المرور الجديده غير مطابقة", 0);
                    }
                } else {
                    makeRed(tf_oldTeacherPassword);
                    jOptionPaneMessage("غير مطابق لكلمة المرور الحالية", 0);
                }

            }
        }
    }//GEN-LAST:event_btn_updatePasswordActionPerformed

