    public void openOrCloseSearchJTextField() {
        String level_name = cb_level1.getSelectedItem().toString();
        String group_name = cb_group.getSelectedItem().toString();
        String class_number = cb_classNumber.getSelectedItem().toString();

        if (!level_name.equals("اختر الفرقة") && !group_name.equals("اختر المجموعة") && !class_number.equals("اختر رقم الحصة")) {
            tf_code.setEnabled(true);
            tf_studentName.setEnabled(true);

        } else {
            tf_code.setEnabled(false);
            tf_studentName.setEnabled(false);
            tf_code.setText("");
            tf_studentName.setText("");
        }
    }

