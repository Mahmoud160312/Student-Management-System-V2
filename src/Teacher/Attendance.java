    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        String level_name = "";
        int class_number = -1;
        if (adminAccept()) {
            TableModel model1 = t_allClass.getModel();
            int[] index = t_allClass.getSelectedRows();
            if (index.length > 0) {
                level_name = cb_level.getSelectedItem().toString();
                class_number = Integer.parseInt(String.valueOf(model1.getValueAt(index[0], 2)));
                try {
                    PreparedStatement stmt = con.prepareStatement("delete from class where level_name=? and class_number=?");
                    stmt.setString(1, level_name);
                    stmt.setInt(2, class_number);
                    stmt.execute();
                    deleteMessage();
                    recordEvent("حذف", "حذف حصة", "تم حذف الحصه رقم " + class_number + "للفرقة " + level_name);
                    // clean panel components
                    loadClass(t_allClass, level_name);
                    cleanAfterAddOrDeleteOrUpdateClass();
                } catch (SQLException ex) {
                    handleSqlExceptions(ex.getErrorCode());
                }
            } else {
                jOptionPaneMessage("تاكد من اختيار احد الفرق من الجدول", 2);
            }
        } else {
            recordEvent("فشل", "حذف حصة", "فشل حذف الحصه رقم " + class_number + "للفرقة " + level_name);

        }
    }//GEN-LAST:event_btn_deleteActionPerformed

